package br.com.zup;

public class Exercicio3 {
    public static void main(String[] args) {

        //Para convertermos metros para centímetros, precisamos multiplicar o número representado em metros por 100.

        float metro1 = 10;
        float centimetro1 = 100;
        float resultado = metro1 * centimetro1;

        System.out.println("O total de centímetros em 10 metros é: " + resultado);
    }
}
